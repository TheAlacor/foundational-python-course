

# method to give a specific range of values: range()
numbers = list(range(200))

# method that change the content of a list in a sentence: join([])
sentence = ' '.join(['Hello', 'world,', 'good', 'morning'])
print(sentence)

# method that sum all the values from the list: sum()
sum = sum(numbers)
print(sum)
# method that show the highest value from the list: max()
max = max(numbers)
print(max)
# method that show the lowest value from the list: min()
min = min(numbers)
print(min)

# method that show how many values are in the list: leng()
quantity = len(numbers)
print(quantity)
