# methods;
list_number = [1,2,3,4,5]
print(list_number)
# index
print("Method: index ")
print(list_number.index(2)) # search index from inserted value
print(list_number.index(3,0,4)) # [num:start:finish]

# in
print("Method: in ")
print(2 in list_number) # true
print(10 in list_number) # false

#count
print("Method: count ")
print(list_number.count(5)) # count how many times a number appear in the list

