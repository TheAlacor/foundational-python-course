# Truthy(true)
print(bool(True))
print(bool(1))
print(bool(0.23))  # all non-zero numbers
print(bool(1j))
print(bool("hello"))
print(bool([1,2,3]))
print(bool((1,2,3)))
print(bool({1,2,3}))

# Falsey(false)
print(bool(False))
print(bool(0))
print(bool(0.0))
print(bool(0j))
print(bool(""))
print(bool([]))
print(bool(()))
print(bool({}))
print(bool(None))




