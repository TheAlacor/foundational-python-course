# equality
print(5 == 5) # true
print(5 == 6) # false
print("hello" == "hello") # true

# inequality
print(5 != 6) # true
print(2 != 2) # false
print("hello" != "world") # true
 
# "greater than" operator (>)
print(4 > 3) # true
print(6 > 9) # false

# "less than" operator (<)
print(3 < 7) # true
print(4 < 1) # false

# "greater than or equal to" operator (>=)
print(7 >= 5) # true
print(5 >= 7) # false
print(5 >= 5) # true

# "greater than or equal to" operator (>=)
print(4 <= 5) # true
print(9 <= 7) # false
print(5 <= 5) # true